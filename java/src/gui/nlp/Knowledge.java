package gui.nlp;

public class Knowledge
{
    private String verb, product, unity, extra, question;

    public String
    getQuestion ()
    {
        return question;
    }

    public void
    setQuestion (String question)
    {
        this.question = question;
    }

    public void
    setVerb (String verb)
    {
        this.verb = verb;
    }

    public String
    getVerb ()
    {
        return verb;
    }

    public void
    setProduct (String product)
    {
        this.product = product;
    }

    public String
    getProduct ()
    {
        return product;
    }

    public void
    setUnity (String unity)
    {
        this.unity = unity;
    }

    public String
    getUnity ()
    {
        return unity;
    }

    public void
    setExtra (String extra)
    {
        this.extra = extra;
    }

    public String
    getExtra ()
    {
        return extra;
    }
}
